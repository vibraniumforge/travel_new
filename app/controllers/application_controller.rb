require './config/environment'


class ApplicationController < Sinatra::Base

  configure do
    set :public_folder, 'public'
    set :views, 'app/views'
    enable :sessions
    set :session_secret, "password_security"
  end

  get '/' do
    flash[:notice] = "Thanks for your email. I'll be in touch soon."
    erb :'index'
  end

end
